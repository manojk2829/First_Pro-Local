package javaPro;

public class Encapsulation_Class {
	
   private String xpath;
   private int count;
   
   public String getXpath() {
	   return xpath;
   }
   
   public int getCount() {
	   return count;
   }
   
   public void setXpath(String xpath) {
	   this.xpath=xpath;
   }
   
   public void setCount(int count) {
	   this.count=count;
   }
	

}
